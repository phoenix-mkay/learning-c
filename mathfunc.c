#include <stdio.h>
#include <math.h>

int main(){
    //  Find square root 
    printf("%f\n",sqrt(16));
    // round number upwards to its nearest integer using ceil
    printf("%f\n",ceil(1.4));
    // round number downwards to its nearest integer using floor
    printf("%f\n",floor(1.4));

    // The pow() function returns the value of x to the power of y (xy):
    printf("%f",pow(4,3));
    return 0;
}